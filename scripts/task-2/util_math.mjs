"use strict";

/**
 * Функция сложения
 * @param {number} numberOne первое число
 * @param {number} numberTwo второе число
 * @returns {number} сумма
 */
export function sum(numberOne, numberTwo) {
    return numberOne + numberTwo;
}

/**
 * Функция вычитания
 * @param {number} numberOne первое число
 * @param {number} numberTwo второе число
 * @returns {number} разница
 */
export function minus(numberOne, numberTwo) {
    return numberOne - numberTwo;
}

/**
 * Функция деления
 * @param {number} numberOne первое число
 * @param {number} numberTwo второе число
 * @returns {number} деление
 */
export function division(numberOne, numberTwo) {
    return numberOne / numberTwo;
}

/**
 * Функция умножение
 * @param {number} numberOne первое число
 * @param {number} numberTwo второе число
 * @returns {number} умножение
 */
export function multiplication(numberOne, numberTwo) {
    return numberOne * numberTwo;
}